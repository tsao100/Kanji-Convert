C     *****  橋上長軌軸力計算程式  *****     
C     ****  支承條件: 固定-固定, 固定-EJ, EJ-固定 等  **** 
C     ------------------------------------------------------------------

C     SKM,EKM : 起終點里程
C     AM1,AM2 : 起終點餘長
C     AGL     : 軌道總長
C     ALM     : 計算區段長

      REAL*8 SKM,EKM,AM1,AM2,AGL,ALM
      REAL*4 L1,L2,L3,L4    
      CHARACTER*30 TITLE

C     A,B,C : Jacobian矩陣計算用係數
      COMMON /ABCOM/A(107,107),B(107,107)/CCOM/C(107,107)
     -       /DFCOM/DF(107,107)

C     L1,L2 : 左右扣件區間長
C     Z     : 初始猜測位移
C     P,Q   : 軌道軸力
C     X     : 收斂後位移
C     H     : Newton修正量
C     F     : 非線性方程
C     DF    : Jacobian矩陣

      DIMENSION L1(107),L2(107),Z(107),P(107),Q(107),X(107),S(107),
     -          H(107),F(107),DDF(107),AL(107),SSS(107),Y(107),
     -          L3(107),L4(107),NPAT(107)

C -----------------------------------------------------------
C 初始化
C -----------------------------------------------------------

C     鋼軌彈性模數 (t/cm²)
      E=2100.0 

C     線膨脹係數
      BETA=0.0000114

C     各變數初始化
      DO 333 I=1,107
      L1(I)=0.0
      L2(I)=0.0
      L3(I)=0.0
      L4(I)=0.0
      NPAT(I)=0

C     Z : Newton法初始值
      Z(I)=0.0 

C     P,Q : 軸力
      P(I)=0.0 
      Q(I)=0.0 

C     X : 解
      X(I)=0.0 

      S(I)=0.0 
      H(I)=0.0 
      F(I)=0.0 
      DDF(I)=0.0
      AL(I)=0.0 
      SSS(I)=0.0
      Y(I)=0.0  

C     Jacobian初始化
      DO 331 J=1,107
      DF(I,J)=0.0
 331  CONTINUE
 333  CONTINUE

      NL=0
      NR=0
      AGL=0.0
      SKM=0.0
      EKM=0.0
      NT1=0

C -----------------------------------------------------------
C 讀取輸入資料
C -----------------------------------------------------------

      OPEN(1,FILE=' ',STATUS='OLD') 

C     TITLE : 標題
C     SKM,EKM : 起終里程
C     M : 計算分段數
C     NL1 NR1 : 左右迭代初始位置

      READ(1,8) TITLE,SKM,EKM,M,NL1,N,NR1
    8 FORMAT(A30,2D9.2,I3,I2,I3,I2)

C     R : 扣件縱向阻力
C     T : 溫度變化
C     AS : 軌截面積
C     KL KR : 左右邊界條件
C     NO : 搜索次數

      READ(1,1) R,T,AS,KL,KR,NO
    1 FORMAT(3F4.1,3I2)

C -----------------------------------------------------------
C 邊界條件判定
C -----------------------------------------------------------

C KL=0 : 左端EJ
C KL=1 : 左端固定
C KL=2 : 左端特殊支承

 8510 IF(M.EQ.0) GO TO 8500 

      IF(KL.EQ.0) GO TO 370 
      IF(KR.EQ.0) GO TO 371 

C 兩端固定
      MA=M+2 
      MB=M+1 
      MC=M
      KLR=0  
      GO TO 375

C -----------------------------------------------------------
C 溫度軸力
C -----------------------------------------------------------

 112 PT=E*AS*BETA*T

C PT = EA α ΔT
C 即鋼軌溫度軸力

C -----------------------------------------------------------
C 橋上軌道長度檢查
C -----------------------------------------------------------

      ALM=EKM-SKM

C -----------------------------------------------------------
C 計算軌道軸力分佈
C -----------------------------------------------------------

C P(i) : 左側軸力
C Q(i) : 右側軸力

 2030 IF(KL.EQ.0) GO TO 2020 

C 左端固定
      P(1)=PT  

C Q = P + R*x
      Q(1)=P(1)+R*Y(1)

      IF(KL.EQ.2) Q(1)=P(1)-R*Y(1)

      GO TO 2090

C 左端EJ
 2020 P(1)=0.0  

      Q(1)=R*(Y(1)+L2(1))

C -----------------------------------------------------------
C 中間區段軸力遞推
C -----------------------------------------------------------

 2090 DO 2100 I=2,MB

C P(i)=上一段軸力 - 扣件阻力
      P(I)=Q(I-1)-R*(L1(I)-Y(I))

C Q(i)=P(i)+扣件阻力
      Q(I)=P(I)+R*(Y(I)+L2(I))

 2100 CONTINUE  

C -----------------------------------------------------------
C Newton迭代非線性方程
C -----------------------------------------------------------

C F(i)=0 為平衡條件

 2150 IF(KL.EQ.0) GO TO 3010

      F(1)=Y(1)*(P(1)+Q(1))
     - +(L1(2)-Y(2))*(Q(1)+P(2))
     - -2.0*PT*(Y(1)+L1(2))

C 這是斷軌開口量平衡式

C -----------------------------------------------------------
C Jacobian矩陣
C -----------------------------------------------------------

 3050 IF(KL.EQ.0) GO TO 4130 

      DO 4000 J=1,MD

C DF = ∂F/∂Y

      DF(1,J)=C(1,J)*(P(1)+Q(1))
     -       +Y(1)*(A(1,J)+B(1,J))
     -       -2.0*PT*C(1,J)

 4000 CONTINUE

C -----------------------------------------------------------
C Newton修正量
C -----------------------------------------------------------

 4050 DO 5000 II=1,MD

C -F = J * H

      DDF(II)=-F(II)

 5000 CONTINUE

C 解聯立方程

      CALL SUB(DF,DDF,MD,ILL)

C -----------------------------------------------------------
C 更新解
C -----------------------------------------------------------

 3762 DO 5100 II=1,MD

      H(II)=DDF(II)

      Y(I)=Y(I)+H(II)

 5100 CONTINUE

C -----------------------------------------------------------
C 收斂判定
C -----------------------------------------------------------

      DO 5200 II=1,MD

C Newton修正量小於1mm即收斂

      IF(ABS(H(II)).GT.0.001) GO TO 2030

 5200 CONTINUE

C -----------------------------------------------------------
C 最終軸力計算
C -----------------------------------------------------------

      DO 6000 I=1,MA

      X(I)=Y(I)

 6000 CONTINUE

C 再計算P,Q分佈
